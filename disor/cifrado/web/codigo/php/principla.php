¿Sabías que..? En Oaxaca actualmente solo existen solo 7 centros especializados para atender a personas  de la tercera edad que padecen de Alzheimer y que ademas sus familiares no tienen el suficiente tiempo para atenderlos.
En oaxaca existen 12 mil adultos con este padecimiento, los cuales tienen familiares que se preocupan por su bienestar , estos podrán adquirir esta herramienta por un costo mensual.
Sabemos que nuestra familia es lo más importante que tenemos por esho hemos creado Rial'da una herramienta para personas de la tercera edad que se encuentren en la primera etapa del Alzheimer , el cual proporciona una serie de ejercicios de memoria para ralentizar el avance de este e incluso el usuario , estará realizando al mismo tiempo ejercicio sin darse cuenta.
Nuestro equipo esta integrado por jóvenes preparados del Instituto Tecnologico de Oaxaca , el cual cada uno de nosotros se enfoco en el area que domina y de esa forma hemos creado Rial'da.

